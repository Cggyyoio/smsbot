def detect_country(phone: str) -> tuple:
    """يرجع (country_code, flag, name) من رقم الهاتف"""
    phone = phone.lstrip("+").lstrip("0")
    for length in (3, 2, 1):
        prefix = phone[:length]
        if prefix in COUNTRY_DATA:
            flag, name = COUNTRY_DATA[prefix]
            return prefix, flag, name
    return "?", "🌍", "غير محدد"
